# create 2 integer variables
number_one = 9
number_two = 4

# addition
answer_addition = number_one + number_two

# subtraction
answer_subtraction = number_one - number_two

# mulitplication
answer_multiplication = number_one * number_two

# division
answer_division = number_one / number_two

# exponent (raising to a power)
answer_exponent = number_one ** number_two

# modulo
answer_modulo = number_one % number_two

# print the answers
print('number_one + number_two =', answer_addition)
print('number_one - number_two =', answer_subtraction)
print('number_one * number_two =', answer_multiplication)
print('number_one / number_two =', answer_division)
print('number_one ** number_two =', answer_exponent)
print('number_one % number_two =', answer_modulo)
